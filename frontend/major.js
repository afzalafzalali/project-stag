window.addEventListener('load', function() {
  const loader = document.getElementById('loader');
  const loadingText = document.getElementById('loadingText');
  let loadProgress = 0;

  // Simulate loading progress up to 100%
  const interval = setInterval(() => {
      loadProgress += 1;
      loadingText.innerText = `${loadProgress}%`;

      // Stop when it reaches 100%
      if (loadProgress === 100) {
          clearInterval(interval);
          
          // Fade out loader smoothly
          loader.style.opacity = '0';
          setTimeout(() => loader.style.display = 'none', 500); // Hide loader after fade out
      }
  }, 30); // Adjust timing here for speed of counter (30ms gives ~3 seconds total)
});



